// program showing the concept of operators
// arithmetic operator

class Demo6{
    public static void main(String args[]){
        int a = 5,b = 2, c;
        c = a + b;
        System.out.println(a+" + "+b+" = "+c);
        c = a - b;
        System.out.println(a+" - "+b+" = "+c);
        c = a * b;
        System.out.println(a+" * "+b+" = "+c);
        c = a / b;
        System.out.println(a+" / "+b+" = "+c);
        System.out.println(a+" / "+b+" = "+((float)a/b));
        c = a % b;
        System.out.println(a+" % "+b+" = "+c);
    }
}
