// program showing the concept of operators
// Arithmetic Assignment operator

class Demo8{
    public static void main(String args[]){
        int a = 5,b = 625;
        
        a += 100; // a = a + 100;
        System.out.println("Result : "+a);

        b /= 25; // b = b / 25;
        System.out.println("Result : "+b);

    }
}
